public class Conta1a100 {
    
    public static void main(String[] args) {
        
        System.out.println("Numeros de 1 a 100:");
        int cont = 1; 

        while (cont <= 100) {

            System.out.println(cont + " ");
            cont = cont + 1;
        }

            System.out.println("\n\nDe novo...");
            for (cont = 1; cont <=100; cont ++){

                System.out.println(cont + " ");
            }
    }
}
